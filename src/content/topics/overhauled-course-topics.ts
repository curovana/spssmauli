const cheat = (id: string, title: string, bullets: string[]): CheatSheetSection => ({ id, title, bullets });
const trap = (id: string, trapText: string, fix: string, skillTag: string): CommonTrap => ({
  id,
  trap: trapText,
  fix,
  skillTag,
});

const opt = (id: string, label: string, explanation: string): QuestionOption => ({ id, label, explanation });

function mcq(
  id: string,
  stem: string,
  skillTags: string[],
  options: QuestionOption[],
  correctOptionId: string,
  explanation: string,
): MCQQuestion {
  return { type: "mcq", id, stem, skillTags, options, correctOptionId, explanation };
}

function testSelection(
  id: string,
  stem: string,
  scenario: string,
  skillTags: string[],
  options: QuestionOption[],
  correctOptionId: string,
  explanation: string,
): TestSelectionQuestion {
  return { type: "test_selection", id, stem, scenario, skillTags, options, correctOptionId, explanation };
}

function calc(
  id: string,
  stem: string,
  skillTags: string[],
  given: string[],
  formula: string,
  correctAnswer: string,
  acceptedAnswers: string[],
  explanation: string,
): CalculationQuestion {
  return { type: "calculation", id, stem, skillTags, given, formula, correctAnswer, acceptedAnswers, explanation };
}

function fill(
  id: string,
  stem: string,
  skillTags: string[],
  answerBank: string[],
  blanks: FillBlankQuestion["blanks"],
  fullCorrectText: string,
): FillBlankQuestion {
  return { type: "fill_blank", id, stem, skillTags, answerBank, blanks, fullCorrectText };
}

function drag(
  id: string,
  stem: string,
  skillTags: string[],
  answerBank: string[],
  blanks: DragDropQuestion["blanks"],
  fullCorrectText: string,
): DragDropQuestion {
  return { type: "drag_drop", id, stem, skillTags, answerBank, blanks, fullCorrectText };
}

function tableQuestion(
  id: string,
  stem: string,
  skillTags: string[],
  table: OutputTable,
  options: QuestionOption[],
  correctOptionId: string,
  explanation: string,
): TableInterpretationQuestion {
  return { type: "table_interpretation", id, stem, skillTags, table, options, correctOptionId, explanation };
}

type LevelArgs = {
  id: string;
  title: string;
  number: number;
  difficulty?: LearningLevel["difficulty"];
  objective: string;
  explanation: string;
  items: string[];
  takeaway: string;
  rules: string[];
  workedTitle: string;
  workedScenario: string;
  workedSteps: string[];
  workedAnswer: string;
  active: Question;
  drill: Question;
  spss?: SPSSWorkflow;
  mistakes: string[];
  sourceRef: string;
};

function level(args: LevelArgs): LearningLevel {
  return {
    id: args.id,
    title: args.title,
    levelNumber: args.number,
    difficulty: args.difficulty ?? "easy",
    sourceRef: args.sourceRef,
    objective: args.objective,
    plainEnglishExplanation: args.explanation,
    visualSummary: {
      title: "Exam pattern",
      kind: "flow",
      items: args.items,
      takeaway: args.takeaway,
    },
    keyRules: args.rules,
    spssSteps: args.spss,
    workedExample: {
      title: args.workedTitle,
      scenario: args.workedScenario,
      steps: args.workedSteps,
      answer: args.workedAnswer,
    },
    activeRecall: [args.active],
    examDrills: [args.drill],
    commonMistakes: args.mistakes,
  };
}

