const withSource = (question: Question, sourceRef?: string): Question =>
  sourceRef && !question.sourceRef ? { ...question, sourceRef } : question;

const asQuestions = (questions: Question | Question[], sourceRef?: string): Question[] =>
  (Array.isArray(questions) ? questions : [questions]).map((question) => withSource(question, sourceRef));

const level = ({
  id,
  number,
  title,
  objective,
  explanation,
  items,
  takeaway,
  rules,
  activeRecall,
  examDrill,
  spssSteps,
  sourceRef,
  workedExample,
  formulas,
  mistakes,
}: {
  id: string;
  number: number;
  title: string;
  objective: string;
  explanation: string;
  items: string[];
  takeaway: string;
  rules: string[];
  activeRecall: Question | Question[];
  examDrill: Question | Question[];
  spssSteps?: SPSSWorkflow;
  sourceRef?: string;
  workedExample?: WorkedExample;
  formulas?: FormulaCard[];
  mistakes?: string[];
}): LearningLevel => ({
  id,
  title,
  levelNumber: number,
  difficulty: number <= 2 ? "easy" : number <= 4 ? "medium" : "hard",
  sourceRef,
  objective,
  plainEnglishExplanation: explanation,
  visualSummary: {
    title: "Exam pattern",
    kind: "flow",
    items,
    takeaway,
  },
  keyRules: rules,
  formulas,
  spssSteps,
  workedExample: workedExample ?? {
    title: `${title} in an exam question`,
    scenario: `Use this level when the question asks you to ${objective.toLowerCase()}.`,
    steps: items,
    answer: takeaway,
  },
  activeRecall: asQuestions(activeRecall, sourceRef),
  examDrills: asQuestions(examDrill, sourceRef),
  commonMistakes: mistakes ?? ["Choosing a test before identifying the outcome variable."],
});

